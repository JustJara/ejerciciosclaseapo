#Crear un programa que pida al usuario ingresar dos números y calcule su suma, resta, multiplicación y división.

print("Hola usuario, este programa es una pequeña calculadora")
numero1= int(input("Ingrese el primer número: "))
numero2= int(input("Ingrese el segundo número: "))

suma_numeros= numero1+numero2
print("La suma de los numeros ingresados es: ",suma_numeros)
resta_numeros= numero1-numero2
print("La resta de los numeros ingresados es: ",resta_numeros)
multiplicacion_numeros= numero1*numero2
print("La multiplicacion de los numeros ingresados es: ",multiplicacion_numeros)
division_numeros= numero1/numero2
print("La división de los numeros ingresados es: ",division_numeros)

